package com.UU.uuaccelerator.model;

public class Spot {

	  public float spotX;
	  public float spotY;
	public Spot(float spotX, float spotY) {
		super();
		this.spotX = spotX;
		this.spotY = spotY;
	}
	public float getSpotX() {
		return spotX;
	}
	public void setSpotX(float spotX) {
		this.spotX = spotX;
	}
	public float getSpotY() {
		return spotY;
	}
	public void setSpotY(float spotY) {
		this.spotY = spotY;
	}
	
}
